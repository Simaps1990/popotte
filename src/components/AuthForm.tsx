import { useState, useEffect } from 'react'
import { signIn, signUp } from '../lib/auth'
import { useNavigate } from 'react-router-dom'
import { supabase } from '../lib/supabaseClient'

type AuthMode = 'login' | 'signup'

declare global {
  interface Window {
    grecaptcha: any;
  }
}

export function AuthForm() {
  const [mode, setMode] = useState<AuthMode>('login')
  const [email, setEmail] = useState('')
  const [username, setUsername] = useState('')
  const [firstName, setFirstName] = useState('')
  const [lastName, setLastName] = useState('')
  const [password, setPassword] = useState('')
  const [error, setError] = useState('')
  const [loading, setLoading] = useState(false)
  const [captchaNum1, setCaptchaNum1] = useState(0)
  const [captchaNum2, setCaptchaNum2] = useState(0)
  const [captchaAnswer, setCaptchaAnswer] = useState('')
  const [captchaError, setCaptchaError] = useState('')
  const navigate = useNavigate()

  // G√©n√©rer des nombres al√©atoires pour le CAPTCHA
  useEffect(() => {
    generateCaptcha()
  }, [])

  const generateCaptcha = () => {
    const num1 = Math.floor(Math.random() * 10) + 1
    const num2 = Math.floor(Math.random() * 10) + 1
    setCaptchaNum1(num1)
    setCaptchaNum2(num2)
    setCaptchaAnswer('')
    setCaptchaError('')
  }

  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault()
    console.log('Soumission du formulaire en cours...')
    setError('')
    setCaptchaError('')
    setLoading(true)

    try {
      console.log('Mode:', mode)
      
      // V√©rifications de base
      if (!email || !password) {
        throw new Error('Veuillez remplir tous les champs')
      }
      
      if (mode === 'signup') {
        if (!username) {
          throw new Error('Veuillez choisir un nom d\'utilisateur')
        }
        if (!firstName) {
          throw new Error('Veuillez entrer votre pr√©nom')
        }
        if (!lastName) {
          throw new Error('Veuillez entrer votre nom')
        }
      }

      // V√©rifier le CAPTCHA uniquement pour l'inscription
      if (mode === 'signup') {
        const expectedAnswer = captchaNum1 + captchaNum2
        const userAnswer = parseInt(captchaAnswer)
        console.log('V√©rification CAPTCHA:', { expectedAnswer, userAnswer })
        
        if (isNaN(userAnswer) || userAnswer !== expectedAnswer) {
          setCaptchaError('R√©ponse incorrecte, veuillez r√©essayer')
          generateCaptcha()
          setLoading(false)
          return
        }
      }

      if (mode === 'login') {
        try {
          console.log('üöÄ === D√âBUT PROCESSUS DE CONNEXION AVEC LOGGING EXTENSIF ===')
          console.log('üìß Email:', email)
          console.log('üîê Password pr√©sent:', !!password)
          console.log('‚è∞ Timestamp:', new Date().toISOString())
          
          // V√©rifier l'√©tat avant connexion
          console.log('üîç √âtat avant connexion:')
          console.log('  - URL actuelle:', window.location.href)
          console.log('  - User agent:', navigator.userAgent)
          console.log('  - Local storage keys:', Object.keys(localStorage))
          
          console.log('üîë Appel de signIn...')
          const startTime = Date.now()
          
          // TIMEOUT D'URGENCE - REDIRECTION FORC√âE SI SIGNIN BLOQUE
          console.log('üöë D√©marrage timeout d\'urgence de 5s pour redirection forc√©e')
          const emergencyRedirect = setTimeout(() => {
            console.log('üö® TIMEOUT SIGNIN - REDIRECTION D\'URGENCE ACTIV√âE !')
            console.log('üö® signIn bloqu√© depuis 5s, redirection brutale...')
            
            // V√©rifier si Supabase dit qu'on est connect√©
            supabase.auth.getSession().then(({ data }: { data: any }) => {
              console.log('üö® Session d\'urgence:', data.session ? 'active' : 'inactive')
              if (data.session) {
                console.log('üö® SESSION ACTIVE D√âTECT√âE - REDIRECTION BRUTALE IMM√âDIATE')
                window.location.href = '/'
              } else {
                console.log('üö® Pas de session active, mais redirection quand m√™me...')
                // Redirection de s√©curit√© m√™me sans session
                setTimeout(() => window.location.href = '/', 1000)
              }
            })
          }, 5000)
          
          let signInResult
          try {
            signInResult = await signIn(email, password)
            clearTimeout(emergencyRedirect) // Annuler le timeout si signIn r√©pond
          } catch (signInError) {
            clearTimeout(emergencyRedirect)
            throw signInError
          }
          
          const { user, error } = signInResult
          const endTime = Date.now()
          
          console.log(`‚è±Ô∏è signIn termin√© en ${endTime - startTime}ms`)
          console.log('üìä R√©sultat signIn:', {
            user: user ? {
              id: user.id,
              email: user.email,
              role: user.role
            } : null,
            error: error ? {
              message: error.message,
              status: (error as any).status || 'N/A',
              statusText: (error as any).statusText || 'N/A'
            } : null
          })
          
          if (error) {
            console.error('‚ùå ERREUR DE CONNEXION D√âTECT√âE:')
            console.error('  - Message:', error.message)
            console.error('  - Code:', (error as any).status)
            console.error('  - D√©tails:', error)
            throw error
          }
          
          if (!user) {
            console.error('‚ùå AUCUN UTILISATEUR RETOURN√â MALGR√â ABSENCE D\'ERREUR')
            throw new Error('Aucun utilisateur retourn√© par signIn')
          }
          
          console.log('‚úÖ CONNEXION R√âUSSIE - ANALYSE DE L\'UTILISATEUR:')
          console.log('  - ID utilisateur:', user.id)
          console.log('  - Email:', user.email)
          console.log('  - R√¥le:', user.role)
          console.log('  - M√©tadonn√©es:', user.user_metadata)
          
          // V√©rifier l'√©tat de la session
          console.log('üîç V√©rification session apr√®s connexion...')
          const { data: sessionData } = await supabase.auth.getSession()
          console.log('üìä Session actuelle:', {
            session: sessionData.session ? {
              user_id: sessionData.session.user.id,
              expires_at: sessionData.session.expires_at,
              access_token: sessionData.session.access_token ? 'pr√©sent' : 'absent'
            } : null
          })
          
          setError('‚úÖ Connexion r√©ussie! Redirection en cours...')
          
          console.log('üîÑ === D√âBUT PROCESSUS DE REDIRECTION ===')
          console.log('  - √âtat loading avant redirection:', loading)
          console.log('  - URL cible: /')
          
          // Attendre un court d√©lai pour que la session se stabilise
          console.log('‚è≥ Attente 500ms pour stabilisation session...')
          await new Promise(resolve => setTimeout(resolve, 500))
          
          // V√©rifier √† nouveau la session
          const { data: sessionData2 } = await supabase.auth.getSession()
          console.log('üìä Session apr√®s attente:', {
            session: sessionData2.session ? 'active' : 'inactive',
            user_id: sessionData2.session?.user?.id
          })
          
          console.log('üöÄ REDIRECTION MULTIPLE POUR GARANTIR LE SUCC√àS:')
          
          // M√©thode 1: React Router navigate
          console.log('  1. Tentative navigate("/")')
          navigate('/')
          
          // M√©thode 2: window.location.href (apr√®s d√©lai)
          console.log('  2. Programmation window.location.href apr√®s 100ms')
          setTimeout(() => {
            console.log('  2. Ex√©cution window.location.href = "/"')
            window.location.href = '/'
          }, 100)
          
          // M√©thode 3: window.location.replace (apr√®s d√©lai plus long)
          console.log('  3. Programmation window.location.replace apr√®s 200ms')
          setTimeout(() => {
            console.log('  3. Ex√©cution window.location.replace("/")')
            window.location.replace('/')
          }, 200)
          
          // M√âTHODE 4: REDIRECTION BRUTALE IMM√âDIATE (NOUVEAU)
          console.log('  4. üî• REDIRECTION BRUTALE IMM√âDIATE - AUCUNE √âCHAPPATOIRE !')
          console.log('  4. üî• Current pathname:', window.location.pathname)
          
          // Forcer imm√©diatement
          if (window.location.pathname === '/auth') {
            console.log('  4. üî• FORCE IMM√âDIATE: window.location.href = "/"')
            window.location.href = '/'
          }
          
          // V√©rification forc√©e toutes les 50ms pendant 2 secondes
          let attempts = 0
          const forceRedirect = setInterval(() => {
            attempts++
            console.log(`  4. üî• V√âRIFICATION FORC√âE #${attempts} - pathname:`, window.location.pathname)
            
            if (window.location.pathname === '/auth') {
              console.log(`  4. üî• TENTATIVE FORC√âE #${attempts}: window.location.href = "/"`)
              window.location.href = '/'
            } else {
              console.log(`  4. ‚úÖ REDIRECTION R√âUSSIE apr√®s ${attempts} tentatives !`)
              clearInterval(forceRedirect)
            }
            
            // Arr√™ter apr√®s 40 tentatives (2 secondes)
            if (attempts >= 40) {
              console.log('  4. üí• √âCHEC TOTAL - REDIRECTION IMPOSSIBLE APR√àS 40 TENTATIVES')
              clearInterval(forceRedirect)
            }
          }, 50)
          
          console.log('üèÅ === FIN PROCESSUS DE CONNEXION ===')
          return
          
        } catch (loginError) {
          console.error('üí• === ERREUR CRITIQUE DANS LE PROCESSUS DE CONNEXION ===')
          console.error('  - Type:', typeof loginError)
          console.error('  - Message:', loginError instanceof Error ? loginError.message : 'Erreur inconnue')
          console.error('  - Stack:', loginError instanceof Error ? loginError.stack : 'Pas de stack')
          console.error('  - Objet complet:', loginError)
          console.error('=== FIN ERREUR CRITIQUE ===')
          throw loginError
        }
      } else {
        console.log('Tentative d\'inscription...')
        const { user, error } = await signUp({ 
          email, 
          password, 
          username,
          firstName,
          lastName
        })
        
        if (error) {
          console.error('Erreur lors de l\'inscription:', error)
          throw error
        }
        
        console.log('Inscription r√©ussie, utilisateur:', user)
        setError('Connexion r√©ussie! Redirection en cours...')
        
        // Attendre un peu pour que la session soit bien √©tablie
        console.log('Attente de 1 seconde pour stabiliser la session...')
        await new Promise(resolve => setTimeout(resolve, 1000))
        
        // SOLUTION RADICALE: Forcer le rechargement de la page
        console.log('üîÑ Redirection forc√©e avec rechargement...')
        window.location.href = '/'
      }
    } catch (error) {
      console.error('Erreur lors de la soumission:', error)
      setError(error instanceof Error ? error.message : 'Une erreur inconnue est survenue')
    } finally {
      setLoading(false)
    }
  }

  return (
    <div className="max-w-md mx-auto mt-10 p-6 bg-white rounded-lg shadow-md">
      <h2 className="text-2xl font-bold mb-6 text-center">
        {mode === 'login' ? 'Connexion' : 'Cr√©er un compte'}
      </h2>
      
      {error && (
        <div className="mb-4 p-3 bg-red-100 text-red-700 rounded">
          {error}
        </div>
      )}

      <form onSubmit={handleSubmit} className="space-y-4">
        {mode === 'signup' && (
          <>
            <div>
              <label htmlFor="username" className="block text-sm font-medium text-gray-700">
                Nom d'utilisateur
              </label>
              <input
                id="username"
                type="text"
                value={username}
                onChange={(e) => setUsername(e.target.value)}
                className="mt-1 block w-full rounded-md border-gray-300 shadow-sm focus:border-orange-500 focus:ring-orange-500"
                required
              />
            </div>
            
            <div className="grid grid-cols-2 gap-4">
              <div>
                <label htmlFor="firstName" className="block text-sm font-medium text-gray-700">
                  Pr√©nom
                </label>
                <input
                  id="firstName"
                  type="text"
                  value={firstName}
                  onChange={(e) => setFirstName(e.target.value)}
                  className="mt-1 block w-full rounded-md border-gray-300 shadow-sm focus:border-orange-500 focus:ring-orange-500"
                  required
                />
              </div>
              
              <div>
                <label htmlFor="lastName" className="block text-sm font-medium text-gray-700">
                  Nom
                </label>
                <input
                  id="lastName"
                  type="text"
                  value={lastName}
                  onChange={(e) => setLastName(e.target.value)}
                  className="mt-1 block w-full rounded-md border-gray-300 shadow-sm focus:border-orange-500 focus:ring-orange-500"
                  required
                />
              </div>
            </div>
            
            {/* Champ CAPTCHA */}
            <div className="mt-4">
              <label htmlFor="captcha" className="block text-sm font-medium text-gray-700">
                Combien font {captchaNum1} + {captchaNum2} ?
              </label>
              <input
                id="captcha"
                type="number"
                value={captchaAnswer}
                onChange={(e) => setCaptchaAnswer(e.target.value)}
                className="mt-1 block w-full rounded-md border-gray-300 shadow-sm focus:border-orange-500 focus:ring-orange-500"
                required
                disabled={loading}
              />
              {captchaError && (
                <p className="mt-1 text-sm text-red-600">{captchaError}</p>
              )}
            </div>
          </>
        )}

        <div>
          <label htmlFor="email" className="block text-sm font-medium text-gray-700">
            Email
          </label>
          <input
            id="email"
            type="email"
            value={email}
            onChange={(e) => setEmail(e.target.value)}
            className="mt-1 block w-full rounded-md border-gray-300 shadow-sm focus:border-orange-500 focus:ring-orange-500"
            required
          />
        </div>

        <div>
          <label htmlFor="password" className="block text-sm font-medium text-gray-700">
            Mot de passe
          </label>
          <input
            id="password"
            type="password"
            value={password}
            onChange={(e) => setPassword(e.target.value)}
            className="mt-1 block w-full rounded-md border-gray-300 shadow-sm focus:border-orange-500 focus:ring-orange-500"
            required
            minLength={6}
          />
        </div>

        <div>
          <button
            type="submit"
            disabled={loading}
            className="w-full flex justify-center py-2 px-4 border border-transparent rounded-md shadow-sm text-sm font-medium text-white bg-orange-600 hover:bg-orange-700 focus:outline-none focus:ring-2 focus:ring-offset-2 focus:ring-orange-500 disabled:opacity-50"
          >
            {loading
              ? 'Chargement...'
              : mode === 'login'
              ? 'Se connecter'
              : 'Cr√©er un compte'}
          </button>
        </div>
      </form>

      <div className="mt-4 text-center">
        <button
          onClick={() => setMode(mode === 'login' ? 'signup' : 'login')}
          className="text-sm text-orange-600 hover:text-orange-800"
        >
          {mode === 'login'
            ? "Pas encore de compte ? S'inscrire"
            : 'D√©j√† un compte ? Se connecter'}
        </button>
      </div>
    </div>
  )
}
